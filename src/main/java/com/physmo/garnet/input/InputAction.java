package com.physmo.garnet.input;

/**
 * Integer ID's that can be used to refer to defined input actions
 * (e.g. left on the controller, fire on keyboard)
 * The user is free to ignore these and use their own definitions.
 */
public class InputAction {
    public static int LEFT = 1;
    public static int RIGHT = 2;
    public static int UP = 3;
    public static int DOWN = 4;
    public static int FIRE1 = 5;
    public static int FIRE2 = 6;
    public static int MENU = 7;

}
